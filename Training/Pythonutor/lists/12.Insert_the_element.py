'''Дан список целых чисел, число k и значение C. Необходимо вставить в список
на позицию с индексом k элемент, равный C, сдвинув все элементы, имевшие индекс
не менее k, вправо. Поскольку при этом количество элементов в списке увеличивается,
после считывания списка в его конец нужно будет добавить новый элемент,
используя метод append. Вставку необходимо осуществлять уже в считанном списке,
не делая этого при выводе и не создавая дополнительного списка.'''

a = [int(i) for i in input().split()]
stroka = [int(i) for i in input().split()]
k = stroka[0]
c = stroka[1]
a.append(a[-1])
for i in range(2, len(a)-k):
    a[-i] = a[-i-1]
a[k] = c
print(' '.join([str(i) for i in a]))
